#include <iostream>

int main(){
    int int_Dividend= 7;
    int int_Divisor= 3;
    std::cout<< "加法結果" << int_Dividend+int_Divisor<< std::endl;
    std::cout<< "減法結果" << int_Dividend-int_Divisor<< std::endl;
    std::cout<< "乘法結果" << int_Dividend*int_Divisor<< std::endl;
    std::cout<< "取餘數結果" << int_Dividend%int_Divisor<< std::endl;
    return 0;
}
